while True:
    x1 = int(input('请输入X:'))
    y1 = int(input('请输入Y:'))
    q = (input('+ - * / **:'))
    if q == '+':
        print(x1+y1)
    elif q == '-':
        print(x1-y1)
    elif q == '*':
        print(x1*y1)
    elif q == '/':
        print(x1/y1)
    elif q == '**':
        print(x1**y1)












